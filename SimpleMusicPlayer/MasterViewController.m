// Customize the number of sections in the table view.
- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    return [self.mediaQuery.collections count];
}

// Customize the appearance of table view cells.
- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:CellIdentifier];
        cell.accessoryType = UITableViewCellAccessoryDisclosureIndicator;
    }

    // Configure the cell.
    MPMediaItemCollection *myItemCollection = [self.mediaQuery.collections objectAtIndex: indexPath.row];
    MPMediaItem *representativeItem = myItemCollection.representativeItem;
    
    NSString *songTitle = [representativeItem valueForProperty: MPMediaItemPropertyAlbumTitle];
    
    cell.textLabel.text = songTitle;
    
    MPMediaItemArtwork *mediaItemArtwork = [representativeItem valueForProperty: MPMediaItemPropertyArtwork];
    UIImage *artworkImage = [mediaItemArtwork imageWithSize: CGSizeMake(44, 44)];
    
    cell.imageView.image = artworkImage;
    cell.accessoryType = UITableViewCellAccessoryDisclosureIndicator;
    
    return cell;
}

